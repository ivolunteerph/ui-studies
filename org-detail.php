						<hr>
						<p>The Caucus of Development NGO Networks (CODE-NGO) is the country’s biggest coalition of civil society organizations working for social development, with its 6 national networks and 6 regional networks representing more than 1,600 development NGOs, POs and cooperatives nationwide. CODE-NGO was established in 1990 by the 10 largest NGO networks in the Philippines. It focuses on being one of the trusted national voices of civil society and on advancing the capacities of civil society organizations (CSOs) to exercise transformative leadership. CODE-NGO’s priority advocacies are transparency, accountability, integrity and people’s participation in governance, asset reform, environmental protection, accessible and high quality social services, CSO good governance, official development aid (ODA) reform, and federalism and constitutional reform. </p>
						
						
